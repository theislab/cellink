def main():
    # Printing a welcome message
    print("Welcome to the basic math calculations program!")

    # Basic math calculations
    a = 10
    b = 5
    
    # Addition
    addition = a + b
    print(f"The addition of {a} and {b} is: {addition}")

    # Subtraction
    subtraction = a - b
    print(f"The subtraction of {a} and {b} is: {subtraction}")

    # Multiplication
    multiplication = a * b
    print(f"The multiplication of {a} and {b} is: {multiplication}")

    # Division
    division = a / b
    print(f"The division of {a} by {b} is: {division}")

if __name__ == "__main__":
    main()